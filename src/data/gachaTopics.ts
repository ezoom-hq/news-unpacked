/**
 * 選択されたカテゴリに基づいてランダムなトピックを取得するヘルパー関数
 * @param categories 有効にするカテゴリIDの配列。指定がない場合は全カテゴリから抽選
 * @returns {string} ランダムなトピック文字列
 */
export const getRandomTopic = (categories: TopicCategory[] = ['basic', 'discussion', 'politics', 'philosophy']): string => {
    // 選択されたカテゴリのデータのみを抽出
    const activeData = GACHA_DATA.filter(cat => categories.includes(cat.id));

    // 全ての有効なトピックを1つの配列にフラット化
    const allTopics = activeData.flatMap(cat => cat.topics);

    if (allTopics.length === 0) {
        return "お題が見つかりませんでした。カテゴリ設定を確認してください。";
    }

    const index = Math.floor(Math.random() * allTopics.length);
    return allTopics[index];
};
